package com.takemeout.common;

public class TypeItem {
  public final int id;
  public final String name;
  public final String descriptionEng;
  public final String descriptionIsl;

  public TypeItem(int id, String name, String descriptionEng, String descriptionIsl) {
    this.id = id;
    this.name = name;
    this.descriptionEng = descriptionEng;
    this.descriptionIsl = descriptionIsl;
  }
}
